#include <iostream>
using namespace std;

int main()
{
    int day = 3;

    if (day == 1)
        cout << "Monday" << endl;
    else if (day == 2)
        cout << "Tuesday" << endl;
    else if (day == 3)
        cout << "Wednesday" << endl;
    else
        cout << "Invalid day" << endl;

    return 0;
}
